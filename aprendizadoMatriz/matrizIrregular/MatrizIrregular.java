class MatrizIrregular {

    public static void main(String[] args) {

       String[][] matrizNomes = new String[5][];

       // Matrizes irregulares, permite a criação de um vetor
       // Interno de espaco dinamico a uma posicao da matriz n dimenções
       matrizNomes[0] = new String[1];
       matrizNomes[1] = new String[5];
       matrizNomes[2] = new String[3];

    }

}
